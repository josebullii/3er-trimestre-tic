#Inicio del programa

numero = int(input("Introduce un número: ")) #Introducir un número para comparar

if (numero % 2) == 0: #Calculamos el resto con el módulo
    print("El número es par") #Cuando el resto es igual a 0
else:
    print("El número es impar") #Cuando el resto es distinto de 0

#Fin del programa